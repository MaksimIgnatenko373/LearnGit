package Module1.Exams;

public class Exam2 {
    public static void main(String[] args) {
        System.out.println();
        int radius = 5;
        double area = Math.PI * radius * radius; // Оператор "Math.PI" "автоматом" выдаёт вариант расчёта числом "Пи", чтобы не расписываит отдельной переменной
        System.out.println("Площадь круга = " + area);
    }
}
